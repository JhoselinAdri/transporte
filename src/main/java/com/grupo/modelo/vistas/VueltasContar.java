package com.grupo.modelo.vistas;

/**
 *
 * @author juans
 */
public class VueltasContar {
    private int n;
    private int cuanto;

    public VueltasContar() {
    }

    public VueltasContar(int n, int cuanto) {
        this.n = n;
        this.cuanto = cuanto;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getCuanto() {
        return cuanto;
    }

    public void setCuanto(int cuanto) {
        this.cuanto = cuanto;
    }
    
}
